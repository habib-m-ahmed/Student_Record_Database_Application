    private void jComboBoxCoursesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxCoursesActionPerformed

        if(jComboBoxCourses.getSelectedItem()!=null)
        {
            // get code
            String code = jComboBoxCourses.getSelectedItem().toString();

            Course c = courses.findCourse(code);

            if(c != null)
            {
                // set course data
                jTextFieldCode.setText(c.getCourseCode());
                jTextFieldCourseGrade.setText(String.valueOf(c.getGrade()));
                jTextFieldDescription.setText(c.getCourseDescription());
            }
        }
    }//GEN-LAST:event_jComboBoxCoursesActionPerformed

    private void jButtonAddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddCourseActionPerformed

        try
        {
            // get course data
            String code = jTextFieldCode.getText();
            String grade = jTextFieldCourseGrade.getText();
            String description = jTextFieldDescription.getText();

            // make course
            Course c = new Course(code, Integer.parseInt(grade),description);

            // add course
            courses.addCourse(c);

            // populate combo
            populateCoursesCombo();
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }

    }//GEN-LAST:event_jButtonAddCourseActionPerformed

