    private Student getStudentData()
    {
        // student info
        Student s = new Student();
    try
    {
    s.setFirstName(jTextFieldFirstName.getText().trim());
    s.setLastName(jTextFieldLastName.getText().trim());
    s.setStreetAddress(jTextFieldStreet.getText().trim());
    s.setCity(jTextFieldCity.getText().trim());
    s.setProvince(jTextFieldProv.getText().trim());
    s.setPostalCode(jTextFieldPostal.getText().trim());
    s.setStudentNumber(jTextFieldNumber.getText().trim());
    s.setHomePhoneNumber(jTextFieldPhone.getText().trim());
    s.setDateofBirth(jTextFieldDOB.getText().trim()); 
    s.setGender(jTextFieldGender.getText().trim());
    //jTextFieldAge.getText());
    s.setGrade(Integer.parseInt(jTextFieldGrade.getText().trim()));
    //jTextFieldAverage
    
    // calculate average
    s.calculateAge();
    
    // courses
     s.setCourse(0, jTextFieldCourse1.getText().trim());
     s.setCourse(1, jTextFieldCourse2.getText().trim());
     s.setCourse(2, jTextFieldCourse3.getText().trim());
     s.setCourse(3, jTextFieldCourse4.getText().trim());
     s.setCourse(4, jTextFieldCourse5.getText().trim());
     s.setCourse(5, jTextFieldCourse6.getText().trim());
     s.setCourse(6, jTextFieldCourse7.getText().trim());
     s.setCourse(7, jTextFieldCourse8.getText().trim());
    
    // marks
     s.setMark(0, Double.parseDouble(jTextFieldMark1.getText().trim()));
     s.setMark(1, Double.parseDouble(jTextFieldMark2.getText().trim()));
     s.setMark(2, Double.parseDouble(jTextFieldMark3.getText().trim()));
     s.setMark(3, Double.parseDouble(jTextFieldMark4.getText().trim()));
     s.setMark(4, Double.parseDouble(jTextFieldMark5.getText().trim()));
     s.setMark(5, Double.parseDouble(jTextFieldMark6.getText().trim()));
     s.setMark(6, Double.parseDouble(jTextFieldMark7.getText().trim()));
     s.setMark(7, Double.parseDouble(jTextFieldMark8.getText().trim()));
     
     s.calculateAverage();
     
      return s;
    }
    catch(Exception ex)
    {
        JOptionPane.showMessageDialog(this, ex.getMessage());
        return null;
    }
    
   
    }
    
    
